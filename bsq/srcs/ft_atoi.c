int	ft_atoi(char *str)
{
	int num;
	int i;

	num = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] < '0' || str[i] > '9')
			return (0);
		num = (num * 10) + (str[i] - '0');
		i++;
	}
	return (num);
}
