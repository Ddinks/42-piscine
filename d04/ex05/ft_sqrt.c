int	ft_sqrt(int nb)
{
	int root;

	if (nb == 0)
		return (0);
	if (nb == 1)
		return (1);
	root = 1;
	while (root < 46341 && root > 0)
	{
		if (root * root == nb)
			return (root);
		root++;
	}
	return (0);
}
